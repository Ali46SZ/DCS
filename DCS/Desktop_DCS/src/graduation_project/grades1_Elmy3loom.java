               byte [] personimage =null;

         JFileChooser f =new JFileChooser();
            f.showOpenDialog(null);
            
           file = f.getSelectedFile();
            
            try{
              fin  = new FileInputStream(file);
                
                ByteArrayOutputStream bo = new ByteArrayOutputStream();
                byte [] buf  =new byte[1024];
                for(int i; (i=fin.read(buf))!=-1; )
                {
                  bo.write(buf, 0, i);
                }
                
                personimage =bo.toByteArray();
                ImageIcon im =new ImageIcon(personimage);
                jLabel20.setIcon(new ImageIcon(resizeimage(im.getImage(),jLabel20.getWidth(),jLabel20.getHeight())));
                
            }catch(Exception e)
            {
                
            }
